$("#s1_form_up").on('submit', function(e) {
    e.preventDefault();
    $.ajax({
        type: 'POST',
        url: 'page_edit_data.php',
        data: new FormData(this),
        dataType: 'json',
        contentType: false,
        cache: false,
        processData: false,
        beforeSend: function() {
            $('#submit_s1').attr("disabled", "disabled");
            $('#s1_form_up').css("opacity", ".5");
        },
        success: function(response) {
            if (response.success) {
                Swal.fire("Ажилттай", "Шинэчилэгдлээ", "success");
            } else {
                Swal.fire("Алдаа", "Ямар нэг асуудал байна", "warning");
            }
            $('#s1_form_up').css("opacity", "1");
            $('#submit_s1').removeAttr("disabled");
            console.log(response);
        }
    });
});
